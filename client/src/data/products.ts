// Helper functions
export function getProductById(id: string): Product | undefined {
  return products.find(p => p.id === id);
}

export function getProductsByCategory(category: Product['category']): Product[] {
  return products.filter(p => p.category === category);
}

export function getFeaturedProducts(): Product[] {
  return products.filter(p => p.featured);
}

export function getProductsBySkinType(skinType: string): Product[] {
  return products.filter(p => p.skinTypes.includes(skinType));
}

export function getProductsByConcern(concern: string): Product[] {
  return products.filter(p => p.concerns.includes(concern));
}

export function getTopRatedProducts(limit: number = 10): Product[] {
  return [...products]
    .sort((a, b) => b.rating - a.rating)
    .slice(0, limit);
}
